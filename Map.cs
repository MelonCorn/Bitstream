        // 맵 출력, (벽, 몬스터, 플레이어)
        public Vector2 PrintMap()
        {
            Vector2 startPos = new Vector2();

            for (int y = 0; y < map.GetLength(0); y++)
            {
                for (int x = 0; x < map.GetLength(1); x++)
                {
                    switch ((Tile)map[y, x])
                    {
                        case Tile.None:
                            Console.Write("　");
                            break;
                        case Tile.BaseLayer:
                            Console.ForegroundColor = ConsoleColor.White;
                            Console.Write("▣");
                            break;
                        case Tile.ByteLayer:
                            Console.ForegroundColor = ConsoleColor.Green;
                            Console.Write("▥");
                            break;
                        case Tile.ShortLayer:
                            Console.ForegroundColor = ConsoleColor.Yellow;
                            Console.Write("▤");
                            break;
                        case Tile.IntLayer:
                            Console.ForegroundColor = ConsoleColor.Red;
                            Console.Write("▦");
                            break;
                        case Tile.LongLayer:
                            Console.ForegroundColor = ConsoleColor.Magenta;
                            Console.Write("▩");
                            break;
                        case Tile.Upgrade:
                            Console.ForegroundColor = ConsoleColor.Yellow;
                            Console.Write("★");
                            break;
                        case Tile.Player:
                            Console.Write("●");
                            startPos.x = x;
                            startPos.y = y;
                            break;
                    }
                }
                Console.WriteLine();
            }

            return startPos;
        }

        // 플레이어 위치, 출력 갱신
        public Vector2 UpdatePlayerPos(Vector2 currentPos, Vector2 newPos)
        {
            // 다음 타일
            Tile nextTile = (Tile)map[newPos.y, newPos.x];

            switch (nextTile)
            {
                case Tile.BaseLayer:
                case Tile.ByteLayer:
                case Tile.ShortLayer:
                case Tile.IntLayer:
                case Tile.LongLayer:
                    break;
                case Tile.Upgrade:
                    GameManager.Instance.CurrentGameState = GameState.Upgrade;
                    break;
                default:
                    Console.SetCursorPosition(currentPos.x * 2, currentPos.y);
                    Console.Write("　");
                    Console.SetCursorPosition(newPos.x * 2, newPos.y);
                    Console.Write("●");
                    return newPos;
            }

            return currentPos;
        }
